    /**
     * Metodo que lista los clientes del archivo de texto
     */
    public void ListarClientes() {
        textarea_listado_clientes.setText("");
        ArrayList<Cliente> clientes = new ArrayList<>();
        String datos = null;
        String[] clienteTxt = null;
        try {
            FileReader reader = new FileReader("ListaClientes");
            BufferedReader buffer = new BufferedReader(reader);
            datos = buffer.readLine();
            while ((datos = buffer.readLine()) != null) // Ciclo que imprime cada una de las lineas, que se almacenan en la variable datos.
            {
                clienteTxt = datos.split(",");
                Cliente cliente;
                cliente = new Cliente(clienteTxt[0], clienteTxt[1], clienteTxt[2], clienteTxt[3], Integer.parseInt(clienteTxt[4]));
                clientes.add(cliente);
            }
            for (Cliente obj : clientes) {
                textarea_listado_clientes.append(obj.toString() + "\n");
            }
            reader.close();//se cierra el reader.
        } catch (IOException e) {
            e.printStackTrace();//se imprime la pila de llamadas para ver d�nde est� el error.
        }
    }

    /**
     * Metodo que lista las conversiones del archivo de texto
     */
    public void ListarConversiones() {
        txtarea_lista_conversiones.setText("");
        ArrayList<String> conversiones = new ArrayList<>();
        String datos = null;
        String[] conversionTxt = null;
        try {
            FileReader reader = new FileReader("Conversiones");
            BufferedReader buffer = new BufferedReader(reader);
            datos = buffer.readLine();
            while ((datos = buffer.readLine()) != null) // Ciclo que imprime cada una de las lineas, que se almacenan en la variable datos.
            {
                conversionTxt = datos.split(",");
                String conversion = conversionTxt[0] + " " + conversionTxt[2] + " ";
                conversiones.add(conversion);
            }
            for (String obj : conversiones) {
                txtarea_lista_conversiones.append(obj + "\n");
            }
            reader.close();//se cierra el reader.
        } catch (IOException e) {
            e.printStackTrace();//se imprime la pila de llamadas para ver donde esta el error.
        }
    }

