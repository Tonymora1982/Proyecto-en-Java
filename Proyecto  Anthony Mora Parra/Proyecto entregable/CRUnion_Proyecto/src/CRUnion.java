    public void ListarMonedaDropDown() {
        select_TipoMoneda.removeAllItems();
        SelectMonedaCompra.removeAllItems();
        select_venta.removeAllItems();
        String[] ListaMonedas = conversion_nueva.ListarMonedasC();
        if (ListaMonedas.length != 0) {
            //cambio en la condicion
            for (int i = 0; i < ListaMonedas.length; i++) {
                select_TipoMoneda.addItem(ListaMonedas[i]);
                SelectMonedaCompra.addItem(ListaMonedas[i]);
                select_venta.addItem(ListaMonedas[i]);
            }
        }
    }

