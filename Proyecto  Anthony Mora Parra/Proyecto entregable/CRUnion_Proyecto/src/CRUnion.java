    /**
     * Registra las conversiones que realizan los clientes en el archivo de txt
     *
     * @param idCliente
     * @param Moneda
     * @param monto
     * @param tipo
     */
    public void registroConversionArchivo(String idCliente, String Moneda, double monto, String tipo) {
        String linea = "Identificacion del cliente: " + idCliente + "," + "Moneda comprada: " + Moneda + "," + "Monto comprado: " + monto + "," + tipo + ",";
        try {
            FileWriter writer = new FileWriter("Conversiones Realizadas por el cliente: ", true);
            BufferedWriter buffer = new BufferedWriter(writer);
            buffer.write(linea);
            buffer.newLine();
            buffer.close();
            ListarClientes();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

